package pt.unl.fct.di.novalincs.yanux.scavenger.common.beacons;

import java.util.List;

public class YanuxBrokerBeaconDetails {
    private String id;
    private String type;
    private List<Object> values;
    private int txPower;
    private int rssi;
    private long timestamp;

    public YanuxBrokerBeaconDetails(String id, String type, List<Object> values, int txPower, int rssi, long timestamp) {
        this.id = id;
        this.type = type;
        this.values = values;
        this.txPower = txPower;
        this.rssi = rssi;
        this.timestamp = timestamp;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public List<Object> getValues() {
        return values;
    }

    public void setValues(List<Object> values) {
        this.values = values;
    }

    public int getTxPower() {
        return txPower;
    }

    public void setTxPower(int txPower) {
        this.txPower = txPower;
    }

    public int getRssi() {
        return rssi;
    }

    public void setRssi(int rssi) {
        this.rssi = rssi;
    }

    public long getTimestamp() {
        return timestamp;
    }

    public void setTimestamp(long timestamp) {
        this.timestamp = timestamp;
    }
}
